package com.dl.poly;

public class Eg2 {

	public static void main(String[] args) {
		
		//Operator Overloading
		//Addition
		int a = 10;
		int b = 20;
		System.out.println(a + b);
		
		//Concat
		String s1 = "10";
		String s2 = "20";
		System.out.println(s1 + s2);
		
		String fname = "Sai";
		String lname = "Kiran";
		System.out.println(fname + lname);
	}
}
