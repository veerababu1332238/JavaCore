public class Eg1 {

	// Type of Control Statements / Flow Controls
	// 1.Selection Statements : if, else, else if, switch
	// 2.Iteration Statements : for, while, do while
	// 3.Transfer Statements : break, continue

	public static void main(String[] args) {

		// if else
		if (true) {
			System.out.println("If Block Executed 01");
		} else {
			System.out.println("Dead Code o=01");
		}

		// if else
		if (false) {
			System.out.println("If Block Executed 02");
		} else {
			System.out.println("Dead Code 02");
		}
	}

}
