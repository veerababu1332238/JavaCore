package com.dl.basics;

public class Eg3 {

	public static void main(String[] args) {
		
		//Wrapper Types / Wrapper Classes
		Byte b1 = 10;
		System.out.println(b1);
		
		Short s1 = 20;
		System.out.println(s1);
		
		Integer i1 = 30;
		System.out.println(i1);
		
		Long l1 = 40L;
		System.out.println(l1);
		
		Float f1 = 10.0f;
		System.out.println(f1);
		
		Double d1 = 20.0;
		System.out.println(d1);
		
		Character ch = 'A';
		System.out.println(ch);
		
		Boolean b2 = true;
		System.out.println(b2);
		
	}
}

//Class : Class is blueprint or a template
//In class we have properties, methods, blocks, constructors
//Two types of classes 
//Build In class: String, Arrays etc...
//User Defined classes: Eg1, Eg2 etc..